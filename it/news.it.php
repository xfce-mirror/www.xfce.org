<b>12/07/2003 - XFce 4.0-RC1 è uscita !</b>

<p>Il progetto XFce ha il piacere di annunciare la prima release candidate dell'Ambiente Desktop XFce 4.0 e la sua Piattaforma di Sviluppo.</p>
<p>XFce 4.0 è un nuovo Ambiente Desktop basato sulle Gtk2 per sistemi operativi basati su Unix, 
come Linux, FreeBSD, NetBSD e Solaris.</p>

XFce 4.0 è :

<ul>
<li>Modulare : scegli tra i pacchetti disponibili.</li>
<li>Moderno e compatibile : rispetta lo standard definito da Freedesktop.</li>
<li>Facile da utilizzare e configurare : personalizza il tuo sistema dal gestore centralizzato delle impostazioni.</li>
<li>Visivamente gradevole : utlizza i temi Gtk2 e fornisce molti temi per le icone e le decorazioni delle finestre.</li>
<li>Lean and mean : preserva le risorse di sistema per le applicazioni.</li>
<li>Completo : offre un pannello originale, un window manager, una taskbar, un filemanager 
  con navigatore SMB network integrato, gestione grafica della stampante, un gestore per il background del desktop, 
  molti utili plugin per il pannello, e altro ancora...</li>
</ul>

<p>Per favore, provate questa prima release candidate di XFce 4.0, riporta i possibili bugs e inviate 
suggerimenti a <a href="mailto:xfce4-dev@xfce.org">xfce4-dev at xfce.org</a> (potete iscrivervi <a href="http://lunar-linux.org/mailman/listinfo/xfce4-dev">qui</a>). Questo ci aviuterà a rilasciare la migliore 
versione finale possibile.</p>

<p>La nota di rilascio con informazioni più dettagliate riguardo questa release, sono disponibili <a href="en/rc1_notes_en.html">qui</a>.<br>Cerca come ottenere il software in <a href="index.php?page=download&lang=it">questa pagina</a>.</p>

<p>
<i>Una nota per le persone che aggiornano dalla precedente beta releases :
<ul>
<li>Le librerie contengono alcuni cambiamenti che rendono questa versione incompatibile
  con le precedenti versioni. Tutte le applicazioni utilizzano libxfcegui4 necessario per la ricompilazione.</li>
<li>Il pannello è leggermente cambiato con l'aggiunta di plugin API. Tutti i plugin del pannello necessitano
  di essere aggiornati e ricompilati.</li>
</ul>
Per favore, leggere la nota di rilascio per ottenere più informazioni.</i>
</p>

