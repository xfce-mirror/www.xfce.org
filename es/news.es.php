<p>Xfce 4.2 includes, among others, the following applications :</p>

<ul class="submenu">
<li>Window manager (xfwm4) : the Xfce 4.2 window manager</li>
<li>Panel (xfce4-panel) : it provides easy-to-setup detachable menus and application launchers</li>
<li>It is compatible with the numerous plug-ins provided by the Xfce Goodies Project,
  including the popular taskbar plugin</li>
<li>Desktop manager (xfdesktop) : the desktop background and root window menu manager</li>
<li>Session manager (xfce4-session) : allows you to restore your desktop the way you left it</li>
<li>File manager (xffm) : a fast file manager, with double pane and integrated samba network browser</li>
<li>Utilities (xfce-utils) : provides the Xfce user guide, and, among other tools, xftaskbar4, a separate
  taskbar that provides an intuitive way to manage windows</li>
<li>Application finder (xfce4-appfinder) : a new application, which allows you to search, launch,
  and find information about applications installed on your system</li>
<li>Iconbox (xfce4-iconbox) : an icon box that can be used as an alternative to the taskbar</li>
<li>Settings manager (xfce-mcs-manager and xfce-mcs-plugins) : provides a centralized Multi-Channel
  Settings manager and plugins, which allow instant application of modified settings</li>
<li>Print manager (xfprint) : the print dialog and manager</li>

</ul>

<p>Xfce 4.2 includes its own GTK2 theme engine and window manager themes, bringing homogeneity to both business and personal desktops.</p>

<p><i>About the Xfce Project : The Xfce Project is a collaboration of international developers working together to develop a desktop environment that is both lightweight and full featured. Keeping with the Unix philosophy of small, modular programs that are very good at doing one thing, the Xfce Project has created a desktop environment which consists of lightweight components which can be strung together in virtually any configuration to create a desktop solution that fits any user just right.</i></p>

