<b>12/07/2003</b>

<p><b>XFce 4.0-RC1</b> is out !</p>
<p>El Proyecto XFce está feliz de anunciar el primer candidato del Entorno de Escritorio y Plataforma de Desarrollo XFce 4.0.</p>
<p>XFce 4.0 es un nuevo Entorno de Escritorio basado en Gtk2, para sistemas operativos derivados de Unix. como Linux, FreeBSD, NetBSD o Solaris.</p>
<b>XFce 4.0 es :</b>
<ul>
<li>Modular : elija de los paquetes disponibles.</li>
<li>Moderno y compatible : respeta los estándares de Freedesktop.</li>
<li>Fácil de usar y configurar : personalice su sistema desde el gestor centralizado de configuración.</li>

<li>Visualmente atractivo : usa temas de Gtk2 y proporciona varios temas para iconos y decoraciones para las ventanas.</li>
<li>Ligero : preserva los recursos del sistema para las aplicaciones.</li>
<li>Completo : ofrece un panel original, un gestor de ventanas, una barra de herramientas, un gestor de archivos con un navegador de red SAMBA incorporado, 
un diálogo de impresión, un gestor de fondos de esritorios, varios plugins útiles, y mucho más...</li>
</ul>

<p>Por favor, pruebe está primer candidato de XFce 4.0, and avise de posibles problemas o envíe sugerencias a 
<a href="http://lunar-linux.org/mailman/listinfo/xfce4-dev">xfce4-dev en xfce.org</a> (puede suscribirse <a href="http://lunar-linux.org/mailman/listinfo/xfce4-dev">aquí</a>). 
Esto nos ayudará a realizar la mejor versión final posible.</p>
<p><b>Notas de lanzamiento</b> con más información detallada sobre este candidato está disponible <a href="en/rc1_notes_en.html">aquí</a>.<br>

