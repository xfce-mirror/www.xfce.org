<b>2004/12/23 - Xfce 4.2 Release Candidate 3 released</b>

<p>The Xfce project is proud to announce the release of version 4.2-RC3 of its Desktop Environment and development framework. Xfce is designed for Unix platforms using the GTK+ toolkit, and follows the Freedesktop specifications. It can be downloaded from <a href="index.php?page=download&lang=es">here</a>.</p>

<p>Xfce is the first desktop environment to ship with an easy to use graphical installation wizard, which takes care of compiling and installing both Xfce and contributed packages on your computer. See <a href="http://xfce-installers.os-cillation.com/">here</a> for download links and instructions.</p>

<p>The third Release Candidate, which provides several bugfixes over the second Release Candidate, is a lightweight desktop environment with several features not found in the Xfce 4.0 series, including a brand new session manager, keyboard shortcut and desktop menu graphical editors, multihead support, "kiosk mode" support, a desktop menu plugin for the panel, CUPS and BSD-LPR printing support, and a new icon theme. Moreover, Xfce 4.2 provides "cutting edge" features, such as a compositing manager for xfwm4 (a manager for the new Xorg compositor extension, which brings true transparency to the Xfce desktop). Xfce is now available in more than 25 languages. A complete changelog between Xfce 4.0 and 4.2 is available on <a href="release_notes/4.2_changelog.html" target="_new">this page</a>, and a list of the changes between 4.2-RC2 and 4.2-RC3 is available on <a href="release_notes/4.1.x_changelog.html">this one</a>.</p>

